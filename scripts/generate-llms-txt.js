// Regenerates llms.txt from docs/registry/*.tsx (the ComponentDoc data already used by the docs site).
// Parses statically via the TypeScript compiler API rather than requiring the files, so it never
// executes component code (which would need a DOM) and never goes stale relative to the docs site.
const fs = require('fs');
const path = require('path');
const ts = require('typescript');

const REGISTRY_DIR = path.join(__dirname, '..', 'docs', 'registry');
const OUTPUT_PATH = path.join(__dirname, '..', 'llms.txt');

const INTRO = `# Krill — LLM context

> Krill is a standalone React 17 UI component library (design system), extracted from Skiff's
> nightwatch-ui. Single npm package, styled-components v5. This file is generated by
> \`npm run llms\` from \`docs/registry/*.tsx\` — don't hand-edit it, edit the registry instead.

## Setup

\`\`\`
npm install krill react@^17 react-dom@^17
\`\`\`

\`\`\`tsx
import 'krill/dist/esm/index.css'; // required once, app-wide — krill does not auto-inject it
import { Button } from 'krill';
\`\`\`

React/react-dom are peerDependencies, not bundled. The consuming app's global CSS must define the
theme's CSS custom properties (e.g. \`--bg-l0-solid\`, \`--text-primary\`, \`--accent-blue-primary\`) —
krill only *reads* them via \`getThemedColor()\`, it doesn't ship a default theme stylesheet.

## Conventions

- Every component accepts \`className\`/\`style\` where relevant, and most accept \`forceTheme?:
  ThemeMode\` to render in a fixed theme regardless of the page's ambient one (used by floating
  surfaces like Dropdown, which is always dark).
- Colors are CSS custom properties resolved through \`getThemedColor()\` / \`getAccentColorValues()\`
  — never hardcoded hex values. \`AccentColor\` = green | orange | red | yellow | pink | dark-blue |
  blue. \`Color\` adds primary | secondary | tertiary | disabled | destructive | link | inverse |
  white | black.
- Dates use \`dayjs\` (\`Dayjs\` type), not native \`Date\`, across DateField/DatePicker/TimeField/etc.
- Most hooks are internal-only; the public ones are \`useHotkeys\`, \`useSwipe\`, \`useOnEscapePress\`,
  \`useOnClickOutside\`.
- No test framework or Storybook in this repo.

## Components

`;

function getLiteralText(node) {
  if (!node) return undefined;
  if (ts.isStringLiteralLike(node)) return node.text;
  return node.getText();
}

function extractPropDoc(objLiteral) {
  const prop = {};
  for (const p of objLiteral.properties) {
    if (!ts.isPropertyAssignment(p)) continue;
    const key = p.name.getText();
    if (key === 'required') {
      prop.required = p.initializer.kind === ts.SyntaxKind.TrueKeyword;
    } else {
      prop[key] = getLiteralText(p.initializer);
    }
  }
  return prop;
}

function hasComponentDocShape(objLiteral) {
  const keys = objLiteral.properties.filter(ts.isPropertyAssignment).map((p) => p.name.getText());
  return keys.includes('slug') && keys.includes('props') && keys.includes('importStatement');
}

function extractComponentDoc(objLiteral) {
  const doc = { props: [] };
  for (const p of objLiteral.properties) {
    if (!ts.isPropertyAssignment(p)) continue;
    const key = p.name.getText();
    if (key === 'props' && ts.isArrayLiteralExpression(p.initializer)) {
      doc.props = p.initializer.elements.filter(ts.isObjectLiteralExpression).map(extractPropDoc);
    } else if (key === 'slug' || key === 'title' || key === 'category' || key === 'description' || key === 'importStatement') {
      doc[key] = getLiteralText(p.initializer);
    }
  }
  return doc;
}

function collectComponentDocs(sourceFile) {
  const docs = [];
  function visit(node) {
    if (ts.isVariableDeclaration(node) && node.initializer && ts.isObjectLiteralExpression(node.initializer) && hasComponentDocShape(node.initializer)) {
      docs.push(extractComponentDoc(node.initializer));
    }
    ts.forEachChild(node, visit);
  }
  ts.forEachChild(sourceFile, visit);
  return docs;
}

const allDocs = [];
for (const file of fs.readdirSync(REGISTRY_DIR)) {
  if (!file.endsWith('.tsx')) continue;
  const filePath = path.join(REGISTRY_DIR, file);
  const sourceText = fs.readFileSync(filePath, 'utf8');
  const sourceFile = ts.createSourceFile(filePath, sourceText, ts.ScriptTarget.Latest, true, ts.ScriptKind.TSX);
  allDocs.push(...collectComponentDocs(sourceFile));
}

allDocs.sort((a, b) => a.category.localeCompare(b.category) || a.title.localeCompare(b.title));

const byCategory = {};
for (const doc of allDocs) {
  (byCategory[doc.category] ??= []).push(doc);
}

const escapeCell = (text) => (text ?? '').replace(/\|/g, '\\|');

let out = INTRO;
for (const [category, docs] of Object.entries(byCategory)) {
  out += `### ${category}\n\n`;
  for (const doc of docs) {
    out += `#### ${doc.title}\n\n`;
    out += `${doc.description}\n\n`;
    out += `\`${doc.importStatement}\`\n\n`;
    if (doc.props.length > 0) {
      out += `| Prop | Type | Required | Default | Description |\n`;
      out += `|---|---|---|---|---|\n`;
      for (const prop of doc.props) {
        const type = escapeCell(prop.type);
        const def = prop.default ? `\`${escapeCell(prop.default)}\`` : '';
        out += `| \`${prop.name}\` | \`${type}\` | ${prop.required ? 'yes' : ''} | ${def} | ${escapeCell(prop.description)} |\n`;
      }
      out += '\n';
    }
  }
}

fs.writeFileSync(OUTPUT_PATH, out);
console.log(`Wrote ${allDocs.length} components to ${path.relative(process.cwd(), OUTPUT_PATH)}`);
